// Type Conversion


// Change String into a Number \\
let nmbr = '999'
console.log(nmbr, typeof nmbr)

nmbr = Number(nmbr)
console.log(nmbr, typeof nmbr)



// Change Number into a String \\
let random = 700;
console.log(random, typeof random)
        // Method 1 \\
// random  = random.toString();
        // Method 2 \\
random = String(random)
console.log(random, typeof random)



// Change String into a Decimal \\
let yeth = '3.69'
console.log(yeth, typeof yeth)
yeth = parseFloat(yeth)
console.log(yeth, typeof yeth)